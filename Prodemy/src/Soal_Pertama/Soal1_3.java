package Soal_Pertama;

public class Soal1_3 {

	public static void main(String[] args) {
		
		// SOAL 1.3 Pola Persegi (Angka)
		int a;
		for(int i = 1; i < 7; i++){
			a = 1;
			for (int j = 1; j < 7; j++){
				System.out.print(a);
				a++;
			}
			System.out.println();
		}


	}

}
